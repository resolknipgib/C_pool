void	ft_rev_int_tab(int *tab, int size)
{
	int		i;
	char	t;

	i = 0;
	while (i < size - 1)
	{
		t = tab[i];
		tab[i] = tab[size];
		tab[size] = t;
		i++;
		size--;
	}
}
