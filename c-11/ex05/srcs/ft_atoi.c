long long	ft_atoi(char *str)
{
	long long	z;
	long long	n;

	z = 1;
	n = 0;
	while ((*str == ' ') || (*str == '\t') || (*str == '\n')
		|| (*str == '\v') || (*str == '\f') || (*str == '\r'))
		str++;
	while ((*str == '-') || (*str == '+'))
	{
		if (*str == '-')
			z *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		n = (n * 10) + ((long long)*str - '0');
		str++;
	}
	return (n * z);
}
