long long	ft_sum(long long a, long long b)
{
	return (a + b);
}

long long	ft_sub(long long a, long long b)
{
	return (a - b);
}

long long	ft_mul(long long a, long long b)
{
	return (a * b);
}

long long	ft_div(long long a, long long b)
{
	return (a / b);
}

long long	ft_mod(long long a, long long b)
{
	return (a % b);
}
