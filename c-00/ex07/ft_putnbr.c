#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	int	t;
	int	s;

	s = 1;
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	t = nb / 10;
	t /= 10;
	while (t > 0)
	{
		s *= 10;
		t /= 10;
	}
	t = nb;
	while (s > 0)
	{
		ft_putchar((t / s) + 48);
		t %= s;
		s /= 10;
	}
}
